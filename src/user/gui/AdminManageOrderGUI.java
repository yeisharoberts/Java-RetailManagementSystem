    private void btnAddCartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddCartActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)tblProduct.getModel();
        int selectedRowIndex = tblProduct.getSelectedRow();
        
     
        String productID = model.getValueAt(selectedRowIndex, 0).toString();
        String productName = model.getValueAt(selectedRowIndex, 1).toString();
        String productCategory = model.getValueAt(selectedRowIndex, 2).toString();
        String productMaterial = model.getValueAt(selectedRowIndex, 3).toString();
        int productQuantity = Integer.valueOf(model.getValueAt(selectedRowIndex, 4).toString());
        double productPrice = Double.parseDouble(model.getValueAt(selectedRowIndex, 5).toString());
        String productDescription = model.getValueAt(selectedRowIndex, 6).toString();
        int quantity = Integer.parseInt(txtQuantitySelect.getText());
        
        String cusName = txtCustName.getText();
        String addr = txtAreaAddr.getText();
        String phone = txtCustPhone.getText();
        String email = txtCustEmail.getText();
       
        Admin ad = new Admin();
        Customer cus = new Customer();
        Order os = new Order();
        OrderItem oi = new OrderItem(quantity, productPrice);
        
       
        
        boolean isPurchased = ad.findPurchase(productName);
        
        if(isPurchased == false){
            try{          
                oi.findTotal(productPrice,quantity);
            
                ad.writeFile(productID,productName,productCategory,productMaterial,oi.getProductQuantity(), 
                        oi.getProductPrice(), oi.getProductTotal(),productDescription);
            
                JOptionPane.showMessageDialog(null, "Successfully Added "+quantity+" "+productName +" to Customer Cart!");
                txtQuantitySelect.setText("");
                tblCart.setModel(new DefaultTableModel());
                showCart();
                subTotal();
            }catch(Exception e){
                JOptionPane.showMessageDialog(null,"Oops, Something Went Wrong!","Error!",JOptionPane.ERROR_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null,"Product Exist in Cart! Please Edit Order in Cart!","Error!",JOptionPane.ERROR_MESSAGE);
        }       
    }//GEN-LAST:event_btnAddCartActionPerformed
    
    public void subTotal(){
        DefaultTableModel model = (DefaultTableModel)tblCart.getModel();
        int selectedRowIndex = tblCart.getSelectedRow();
        OrderItem oi = new OrderItem();
        
        for (int i = 0, rows = model.getRowCount(); i < rows; i++)
        {
            oi.calcTotal(Double.parseDouble(model.getValueAt(i, 8).toString()));
        }
        txtSubTotal.setText(Double.toString(oi.getProductTotal()));
    }
    
