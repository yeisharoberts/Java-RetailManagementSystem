    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)tblCart.getModel();
        int selectedRowIndex = tblCart.getSelectedRow();
        
        String orderID = model.getValueAt(selectedRowIndex, 0).toString();
        String productID = model.getValueAt(selectedRowIndex, 1).toString();
        String productName = model.getValueAt(selectedRowIndex, 2).toString();
        String productCategory = model.getValueAt(selectedRowIndex, 3).toString();
        String productMaterial = model.getValueAt(selectedRowIndex, 4).toString();
        int quantitySelected = Integer.valueOf(model.getValueAt(selectedRowIndex, 5).toString());
        double productPrice = Double.parseDouble(model.getValueAt(selectedRowIndex, 6).toString());
        String productDesc = model.getValueAt(selectedRowIndex, 7).toString();
        double total = Double.parseDouble(model.getValueAt(selectedRowIndex, 8).toString());          
        int quantityEdit = Integer.valueOf(txtNewQuantity.getText());
        
        Customer cus = new Customer();
        OrderItem oi = new OrderItem();
        
        oi.findTotal(productPrice, quantityEdit);

        int verify = cus.validateModiProChanges(orderID,productID,productName,productCategory,productMaterial,
            quantitySelected,productPrice,productDesc,oi.getProductTotal(),quantityEdit);

        if (verify==0){
            //Display Message Box
            JOptionPane.showMessageDialog(this,"Product details have been modified.",
            "UPDATE SUCCESSFUL", JOptionPane.INFORMATION_MESSAGE);
            tblCart.setModel(new DefaultTableModel());
            showCart();
            subTotal();
            txtNewQuantity.setText("");
            txtProdName.setText("");
            txtQuantity.setText("");
            txtPrice.setText("");
            txtTotal.setText("");
        }else if (verify == 1){
             JOptionPane.showMessageDialog(this,"There is no modification. ",
            "NO MODIFICATION WERE FOUND", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

