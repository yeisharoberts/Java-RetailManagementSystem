    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // add new product
        boolean v=false;
        File f = new File("Product.txt");
        try {
            // create a file when file doesnt exist
            if(!f.exists()){
                f.createNewFile();
            }
        } catch(IOException Ex){
            System.out.println("File Error");
        }

        // Count the line of file
        int lineCount=0;

        try{
            // Read line
            BufferedReader br = new BufferedReader(new FileReader(f));
            while(br.readLine() != null)
            {
                lineCount++;
            }
            br.close();
        }
        catch(IOException Ex){
            System.out.println("Error! Reading file issues~");
        }

        // auto generate customer ID
        int id = lineCount + 1;
        String productID = "P" + String.format("%03d", id);
        // Customer info that need to be record in the text file
        String proName = txtProductName.getText();
        String category = cbxCategory.getSelectedItem().toString();
        String material = txtMaterial.getText();
        int quantity = Integer.parseInt(txtQuantity.getText());
        Double price = Double.parseDouble(txtPrice.getText());
        String proDesc = txtProDesc.getText();

        // make sure no blank text field in GUI
        if(!"".equals(proName)&& !"".equals(category) && !"".equals(material)&&
            !"".equals(quantity)&& !"".equals(price)&& !"".equals(proDesc)){

            //call method
            Admin cus = new Admin();
            // call customer method to write data into txt file
            int verify = cus.addProduct(productID,proName,category, material, quantity,price,proDesc);

            if(verify == 1 ){
                if(v=true){
                    JOptionPane.showMessageDialog(this,
                            "Product Has Been Added"+ "\n The product ID of '"+ proName+"' is : " + productID);
                    txtProductName.setText("");
                    cbxCategory.setSelectedItem("");
                    txtMaterial.setText("");
                    txtQuantity.setText("");
                    txtPrice.setText("");
                    txtProDesc.setText("");  
                }
            }
            // product exist
            else if (verify == 0){
                v=false;
                JOptionPane.showMessageDialog(this, "The product "+proName+" is already exists ",
                    "Repeated Data",JOptionPane.INFORMATION_MESSAGE );
            }

        }
        else{
            JOptionPane.showMessageDialog(this, "Please complete the form~");
        }

    }//GEN-LAST:event_btnAddActionPerformed

