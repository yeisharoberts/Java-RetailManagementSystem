    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
       // Search customer data
        String findID = txtSearchProductID.getText();
        
        File f = new File("Product.txt");

        boolean valid = false;
        try{
            Scanner data = new Scanner(f);
            while (data.hasNextLine())
            {
                String line = data.nextLine();
                String[] proLine = line.split("\t");
                
                // Check existing of customer
                if(proLine[0].equals(findID)){
                String[] temp = line.split("\t");   
                
                Product proData = new Product(temp[0],temp[1],temp[2],temp[3],Integer.parseInt(temp[4]),Double.parseDouble(temp[5]),temp[6]);

                lblShowProName.setText(proData.getProductName());
                cbxShowCate.setSelectedItem(proData.getCategory());
                txtMaterial.setText(proData.getMaterial());
                txtQuantity.setText(Integer.toString(proData.getQuantity()));
                txtPrice.setText(Double.toString(proData.getPrice()));
                txtProDesc.setText(proData.getProDesc());    
            
                valid=true;
                break;
            }
        }
            data.close();
        //  If Customer ID doesn't exist
        if (valid == false) {
            JOptionPane.showMessageDialog(null, "Product ID does not exist.",
                "Record is not founded!", JOptionPane.ERROR_MESSAGE);
                txtSearchProductID.setText("");
            } 
        }
        catch(IOException Ex) {
            System.out.println("File error: ");
        }
  
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnUpdateProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateProductActionPerformed
        // Get input from user
        String proID = txtSearchProductID.getText();
        String proName = lblShowProName.getText();
        String category= cbxShowCate.getSelectedItem().toString();
        String material = txtMaterial.getText();
        int quantity = Integer.parseInt(txtQuantity.getText());
        double price = Double.parseDouble(txtPrice.getText());
        String proDesc = txtProDesc.getText();
     
        Admin ad = new Admin();
        int verify = ad.validateModiProChanges(proID, proName, category, material, quantity, price, proDesc);

        if (verify==0){
            //Display Message Box
            JOptionPane.showMessageDialog(this,"Product details have been modified. ",
            "UPDATE SUCCESSFUL", JOptionPane.INFORMATION_MESSAGE);
            
            txtSearchProductID.setText("");
            lblShowProName.setText("_______");
            cbxShowCate.setSelectedItem("");
            txtMaterial.setText("");
            txtQuantity.setText("");
            txtPrice.setText("");
            txtProDesc.setText("");
        
        }else if (verify == 1){
             JOptionPane.showMessageDialog(this,"There is no modification. ",
            "NO MODIFICATION WERE FOUND", JOptionPane.INFORMATION_MESSAGE);
         }

     }//GEN-LAST:event_btnUpdateProductActionPerformed

    private void btnDeleteProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteProductActionPerformed
        // Remove product from text file
        String f = "Product.txt";
        String removeTerm = txtSearchProductID.getText();
        
       int dialogBtn = JOptionPane.showConfirmDialog(null,
                "Are you sure want to delete the customer account ?",
                "Confirmation", JOptionPane.YES_NO_OPTION);

        if(dialogBtn == JOptionPane.YES_OPTION){
            Admin ad = new Admin();
            ad.removeRecord(f, removeTerm,1,"\t");
        
            //Display Message Box
            JOptionPane.showMessageDialog(this,"The Product Has Been Deleted. ",
            "SUCCESSFULLY DELETE", JOptionPane.INFORMATION_MESSAGE);  
        
            txtSearchProductID.setText("");
            lblShowProName.setText("_______");
            cbxShowCate.setSelectedItem("");
            txtMaterial.setText("");
            txtQuantity.setText("");
            txtPrice.setText("");
            txtProDesc.setText("");
        }
     
        
        
    }//GEN-LAST:event_btnDeleteProductActionPerformed

